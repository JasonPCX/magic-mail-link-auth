export default function redisReplace(value) {
  const replacements = {
    ",": "\\,",
    ".": "\\.",
    "<": "\\<",
    ">": "\\>",
    "{": "\\{",
    "}": "\\}",
    "[": "\\[",
    "]": "\\]",
    '"': '\\"',
    "'": "\\'",
    ":": "\\:",
    ";": "\\;",
    "!": "\\!",
    "@": "\\@",
    "#": "\\#",
    $: "\\$",
    "%": "\\%",
    "^": "\\^",
    "&": "\\&",
    "*": "\\*",
    "(": "\\(",
    ")": "\\)",
    "-": "\\-",
    "+": "\\+",
    "=": "\\=",
    "~": "\\~",
  };

  const newValue = value.replace(/,|\.|<|>|\{|\}|\[|\]|"|'|:|;|!|@|#|\$|%|\^|&|\*|\(|\)|-|\+|=|~/g, function (x) {
    return replacements[x];
  });
  return newValue;
}
